package com.bekololek.pluginfactory.build;

import com.bekololek.pluginfactory.user.User;
import com.bekololek.pluginfactory.user.UserRepository;
import jakarta.persistence.EntityManager;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.orm.jpa.DataJpaTest;
import org.springframework.test.context.ActiveProfiles;

import java.time.Instant;
import java.time.temporal.ChronoUnit;
import java.util.List;
import java.util.UUID;

import static org.assertj.core.api.Assertions.assertThat;

/**
 * Verifies the derived-query semantics powering {@link BuildRecoveryService}.
 *
 * <p>The repository methods themselves are generated by Spring Data from
 * the method names, so the only thing that can break is the name parsing
 * (e.g. {@code CurrentPhaseNot} → "WHERE current_phase &lt;&gt; ?"). These
 * tests pin that behavior so a future rename can't silently
 * reintroduce the PLAN_REVIEW reaper bug.
 */
@DataJpaTest
@ActiveProfiles("test")
class BuildSessionRepositoryTest {

    @Autowired
    private BuildSessionRepository buildSessionRepository;

    @Autowired
    private UserRepository userRepository;

    @Autowired
    private EntityManager entityManager;

    private static final List<BuildStatus> TRANSIENT_STATUSES = List.of(
            BuildStatus.PLANNING,
            BuildStatus.APPROVED,
            BuildStatus.BUILDING,
            BuildStatus.TESTING
    );

    private UUID userId;

    @BeforeEach
    void seedUser() {
        // build_sessions.user_id is a FK to users.id, so each test needs
        // at least one persisted user before inserting sessions.
        User user = new User();
        user.setEmail("repo-test-" + UUID.randomUUID() + "@example.com");
        user.setDisplayName("Repo Test User");
        user.setAuthProvider("discord");
        user.setDiscordId("repo-test-" + UUID.randomUUID());
        userId = userRepository.saveAndFlush(user).getId();
    }

    @Test
    void findByStatusInAndCurrentPhaseNot_excludesPlanReview() {
        BuildSession planReview = persist(BuildStatus.PLANNING, BuildPhase.PLAN_REVIEW);
        BuildSession planGeneration = persist(BuildStatus.PLANNING, BuildPhase.PLAN_GENERATION);
        BuildSession implementing = persist(BuildStatus.BUILDING, BuildPhase.IMPLEMENTATION);
        BuildSession chatting = persist(BuildStatus.CHATTING, BuildPhase.CLARIFICATION);

        List<BuildSession> result = buildSessionRepository
                .findByStatusInAndCurrentPhaseNot(TRANSIENT_STATUSES, BuildPhase.PLAN_REVIEW);

        assertThat(result)
                .extracting(BuildSession::getId)
                .containsExactlyInAnyOrder(planGeneration.getId(), implementing.getId())
                .doesNotContain(planReview.getId(), chatting.getId());
    }

    @Test
    void findByStatusInAndCurrentPhaseNotAndUpdatedAtBefore_excludesPlanReviewAndRecentSessions() {
        Instant now = Instant.now();
        Instant longAgo = now.minus(30, ChronoUnit.MINUTES);

        // Stale (updated 30 min ago) + PLAN_REVIEW: must be excluded by phase filter.
        BuildSession stalePlanReview = persistWithUpdatedAt(
                BuildStatus.PLANNING, BuildPhase.PLAN_REVIEW, longAgo);

        // Stale + PLAN_GENERATION: must be returned (worker actually wedged).
        BuildSession stalePlanGeneration = persistWithUpdatedAt(
                BuildStatus.PLANNING, BuildPhase.PLAN_GENERATION, longAgo);

        // Stale + IMPLEMENTATION: must be returned.
        BuildSession staleImplementation = persistWithUpdatedAt(
                BuildStatus.BUILDING, BuildPhase.IMPLEMENTATION, longAgo);

        // Recent + IMPLEMENTATION: not stale yet, must be excluded by time filter.
        BuildSession recentImplementation = persistWithUpdatedAt(
                BuildStatus.BUILDING, BuildPhase.IMPLEMENTATION, now);

        // Stale + non-transient status (CHATTING): excluded by status filter.
        BuildSession staleChatting = persistWithUpdatedAt(
                BuildStatus.CHATTING, BuildPhase.CLARIFICATION, longAgo);

        Instant cutoff = now.minus(15, ChronoUnit.MINUTES);

        List<BuildSession> result = buildSessionRepository
                .findByStatusInAndCurrentPhaseNotAndUpdatedAtBefore(
                        TRANSIENT_STATUSES, BuildPhase.PLAN_REVIEW, cutoff);

        assertThat(result)
                .extracting(BuildSession::getId)
                .containsExactlyInAnyOrder(stalePlanGeneration.getId(), staleImplementation.getId())
                .doesNotContain(
                        stalePlanReview.getId(),
                        recentImplementation.getId(),
                        staleChatting.getId());
    }

    private BuildSession persist(BuildStatus status, BuildPhase phase) {
        BuildSession session = new BuildSession();
        session.setUserId(userId);
        session.setStatus(status);
        session.setCurrentPhase(phase);
        return buildSessionRepository.saveAndFlush(session);
    }

    /**
     * Persist then back-date {@code updated_at} via native SQL to bypass
     * the {@code @PreUpdate} hook on {@link com.bekololek.pluginfactory.common.BaseEntity}
     * (which would otherwise stamp it back to "now" on every merge).
     */
    private BuildSession persistWithUpdatedAt(BuildStatus status, BuildPhase phase, Instant updatedAt) {
        BuildSession session = persist(status, phase);
        entityManager.createNativeQuery(
                        "UPDATE build_sessions SET updated_at = ?1 WHERE id = ?2")
                .setParameter(1, updatedAt)
                .setParameter(2, session.getId())
                .executeUpdate();
        entityManager.clear();
        return session;
    }
}
