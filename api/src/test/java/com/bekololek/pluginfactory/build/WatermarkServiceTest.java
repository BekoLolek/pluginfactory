package com.bekololek.pluginfactory.build;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import java.util.Map;
import java.util.UUID;

import static org.assertj.core.api.Assertions.assertThat;

class WatermarkServiceTest {

    private WatermarkService watermarkService;

    @BeforeEach
    void setUp() {
        watermarkService = new WatermarkService();
    }

    @Test
    void watermarkSource_javaFilesGetHeader() {
        UUID sessionId = UUID.randomUUID();
        UUID userId = UUID.randomUUID();
        UUID watermarkId = UUID.randomUUID();

        Map<String, String> sourceFiles = Map.of(
                "src/Main.java", "public class Main {}",
                "src/Helper.java", "public class Helper {}"
        );

        Map<String, String> result = watermarkService.watermarkSource(sourceFiles, sessionId, userId, watermarkId);

        assertThat(result.get("src/Main.java")).startsWith("/*");
        assertThat(result.get("src/Main.java")).contains("Generated by BekoLolek Plugin Factory");
        assertThat(result.get("src/Main.java")).contains("public class Main {}");

        assertThat(result.get("src/Helper.java")).startsWith("/*");
        assertThat(result.get("src/Helper.java")).contains("public class Helper {}");
    }

    @Test
    void watermarkSource_nonJavaFilesUnchanged() {
        UUID sessionId = UUID.randomUUID();
        UUID userId = UUID.randomUUID();
        UUID watermarkId = UUID.randomUUID();

        Map<String, String> sourceFiles = Map.of(
                "pom.xml", "<project></project>",
                "plugin.yml", "name: TestPlugin",
                "config.yml", "key: value"
        );

        Map<String, String> result = watermarkService.watermarkSource(sourceFiles, sessionId, userId, watermarkId);

        assertThat(result.get("pom.xml")).isEqualTo("<project></project>");
        assertThat(result.get("plugin.yml")).isEqualTo("name: TestPlugin");
        assertThat(result.get("config.yml")).isEqualTo("key: value");
    }

    @Test
    void watermarkSource_containsSessionUserAndWatermarkIds() {
        UUID sessionId = UUID.randomUUID();
        UUID userId = UUID.randomUUID();
        UUID watermarkId = UUID.randomUUID();

        Map<String, String> sourceFiles = Map.of(
                "Test.java", "class Test {}"
        );

        Map<String, String> result = watermarkService.watermarkSource(sourceFiles, sessionId, userId, watermarkId);

        String watermarked = result.get("Test.java");
        assertThat(watermarked).contains(sessionId.toString());
        assertThat(watermarked).contains(userId.toString());
        assertThat(watermarked).contains(watermarkId.toString());
    }

    @Test
    void watermarkSource_containsLicenseInfo() {
        UUID sessionId = UUID.randomUUID();
        UUID userId = UUID.randomUUID();
        UUID watermarkId = UUID.randomUUID();

        Map<String, String> sourceFiles = Map.of(
                "Test.java", "class Test {}"
        );

        Map<String, String> result = watermarkService.watermarkSource(sourceFiles, sessionId, userId, watermarkId);

        String watermarked = result.get("Test.java");
        assertThat(watermarked).contains("Personal use only");
        assertThat(watermarked).contains("No redistribution");
        assertThat(watermarked).contains("pluginfactory.bekololek.com/license");
    }

    @Test
    void watermarkSource_mixedFiles() {
        UUID sessionId = UUID.randomUUID();
        UUID userId = UUID.randomUUID();
        UUID watermarkId = UUID.randomUUID();

        Map<String, String> sourceFiles = Map.of(
                "src/Main.java", "public class Main {}",
                "pom.xml", "<project></project>"
        );

        Map<String, String> result = watermarkService.watermarkSource(sourceFiles, sessionId, userId, watermarkId);

        // Java file should have watermark
        assertThat(result.get("src/Main.java")).contains("Generated by BekoLolek Plugin Factory");
        assertThat(result.get("src/Main.java")).contains("public class Main {}");

        // Non-java file should be unchanged
        assertThat(result.get("pom.xml")).isEqualTo("<project></project>");
    }
}
